    private void btn_ResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ResolverActionPerformed
        // TODO add your handling code here:

        try {
            if (iteraciones.validCheck(tbl_Matriz)) {
                int N = 2;
                int rows = 0, columns = 0;
                rows = tbl_Matriz.getRowCount();
                columns = tbl_Matriz.getColumnCount();

                Object object_A[][] = iteraciones.getTableData(tbl_Matriz);
                double m_A[][] = new double[rows][columns];

                for (int i = 0; i < tbl_Matriz.getRowCount(); i++) {
                    for (int j = 0; j < tbl_Matriz.getColumnCount(); j++) {
                        m_A[i][j] = Double.parseDouble((String) object_A[i][j]);
                    }
                }

                double adj[][] = new double[rows][columns];
                double inv[][] = new double[rows][columns];

                Inversa_Proceso proceso = new Inversa_Proceso();
                proceso.adjoint(m_A, adj, rows);

                if (proceso.inverse(m_A, inv, rows)) {
                    iteraciones.mostrar_Matriz(inv, rows, columns, tbl_Resultado1);
                    iteraciones.centrar(tbl_Resultado1);

                    jLabel7.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe llenar todas las celdas");
        }
    }//GEN-LAST:event_btn_ResolverActionPerformed

    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        // TODO add your handling code here:
        int rows = 0, columns = 0;
        try {
            rows = Integer.parseInt(txt_filas.getText());
            columns = Integer.parseInt(txt_Col.getText());

            if (!(rows == columns)) {
                JOptionPane.showMessageDialog(null, "La matriz debe ser cuadrada para calcualar su inversa");
            } else {
                Tabla_Iteraciones matrix_Iteraciones = new Tabla_Iteraciones();
                matrix_Iteraciones.generar(rows, columns, tbl_Matriz);
                matrix_Iteraciones.centrar(tbl_Matriz);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese solo nÃºmeros");
        }
    }//GEN-LAST:event_btn_AceptarActionPerformed

